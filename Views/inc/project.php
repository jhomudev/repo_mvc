    <h1 class="project__title">Ingeniería inversa para la adaptación tecnológica en una empresa manufacturera peruana, estudio de caso</h1>
    <h3 class="projetc__desc__text enc">Descripción del proyecto</h3>
    <p class="project__descri">El sector manufacturero se ha convertido en uno de los ejes de la economía peruana por sus altas tasas de crecimiento a partir de la liberación comercial de la década de los noventa generando nuevos empleos, ingresos fiscales y desarrollo de pequeñas empresas que producen partes y servicios para este sector. Evidentemente, su crecimiento y competitividad está fuertemente relacionado con la innovación tecnológica donde un 65,5% de las empresas manufactureras desarrolló al menos una actividad de innovación en el periodo 2009-2011 (CONCYTEC, 2013). Cabe destacar que estas actividades de innovación tienen como objetivo final mejorar los resultados de la empresa al desarrollar y poner en marcha nuevos productos y procesos ("Oslo manual," 2005). Estas van desde actividades en investigación y desarrollo interno y externo, transferencia tecnológica, adquisiciones de bienes de capital y TIC’s, capacitación, hasta las de diseño e ingeniera industrial. A propósito, el resultado de la primera Encuesta Nacional de Innovación en la Industria Manufacturera muestra que el 78,4% de las empresas centran sus esfuerzos en la adquisición de bienes de capital (INEI, 2012). Esta falta de equilibrio entre las actividades de innovación, pone en riesgo el desarrollo de capacidades endógenas y es un factor que explica el subdesarrollo tal como señala Máximo Vega (2003, p. 32): “una indagación global e histórica sobre las causas del subdesarrollo llevo a evaluar la relación de las economías subdesarrolladas con las desarrolladas (…) se trata esta vez de la dependencia como forma de relación con resultados predeterminados y distribución no equitativa de los frutos”.</p>
